### CONSULTAS CON JOIN ###
# SELECCIONAR EL NOMBRE DE LA CATEGORIA JUNTO CON LOS DATOS DEL TRAILER
SELECT t.idTrailer, t.titulo, t.link, t.resumen, t.poster, t.temporadas, c.nombreCategoria
FROM trailerflix.trailer AS t
INNER JOIN trailerflix.trailerCategoria AS tc ON t.idTrailer = tc.idTrailer
INNER JOIN trailerflix.categoria AS c ON tc.idCategoria = c.idCategoria;

# SELECCIONAR EL GENERO JUNTO CON LOS DATOS DEL TRAILER
SELECT t.idTrailer, t.titulo, t.link, t.resumen, t.poster, t.temporadas, GROUP_CONCAT(g.nombreGenero SEPARATOR ', ') AS generos
FROM trailerflix.trailer AS t
INNER JOIN trailerflix.trailerGenero AS tg ON t.idTrailer = tg.idTrailer
INNER JOIN trailerflix.genero AS g ON tg.idGenero = g.idGenero
GROUP BY t.idTrailer;

# SELECCIONAR LOS ACTORES DE REPARTO JUNTO CON LOS DATOS DEL TRAILER
SELECT t.idTrailer, t.titulo, t.link, t.resumen, t.poster, t.temporadas, GROUP_CONCAT(r.nombreActor SEPARATOR ', ') AS reparto
FROM trailerflix.trailer AS t
INNER JOIN trailerflix.trailerReparto AS tr ON t.idTrailer = tr.idTrailer
INNER JOIN trailerflix.reparto AS r ON tr.idReparto = r.idReparto
GROUP BY t.idTrailer;

# SELECCION COMPLETA, CON CONTROL DE DATOS NULOS Y N/A
SELECT t.idTrailer, t.titulo, COALESCE(t.link, 'Sin link') AS link, t.resumen, t.poster, 
CASE
	WHEN t.temporadas = 'N/A' THEN 'Sin temporadas'
    ELSE t.temporadas
END AS temporadas
, c.nombreCategoria AS categoria, GROUP_CONCAT(DISTINCT g.nombreGenero SEPARATOR ', ') AS generos, GROUP_CONCAT(DISTINCT r.nombreActor SEPARATOR ', ') AS reparto
FROM trailerflix.trailer AS t
INNER JOIN trailerflix.trailerCategoria AS tc ON t.idTrailer = tc.idTrailer
INNER JOIN trailerflix.categoria AS c ON tc.idCategoria = c.idCategoria
INNER JOIN trailerflix.trailerGenero AS tg ON t.idTrailer = tg.idTrailer
INNER JOIN trailerflix.genero AS g ON tg.idGenero = g.idGenero
INNER JOIN trailerflix.trailerReparto AS tr ON t.idTrailer = tr.idTrailer
INNER JOIN trailerflix.reparto AS r ON tr.idReparto = r.idReparto
GROUP BY t.idTrailer;

